package com.tns.program.basic_program;

public class Prog20 
{

	public static void main(String[] args) 
	{
		
		// Nested for loop
		
		for(int i=1;i<=10;i++)
		{
			for(int j = 1;j<=10;j++)
			{
				System.out.println("Value of i is "+i+" and value of j is "+j);
			}
		}
	}

}
