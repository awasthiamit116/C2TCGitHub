package com.tns.program.basic_program;

public class Prog7 {

	public static void main(String[] args) {
		
		int x = 5;
		if(x!=5) // Not equal to
		{
			System.out.println("Value of x is not 5");
		}
		else
		{
			System.out.println("Value of x is 5");
		}
		
		int y = 7;
		if(x>=y) // Greater than equal to
		{
			System.out.println("True");
		}
		else
		{
			System.out.println("False");
		}
		
		int a = 10;
		int b = 11; 
		
		if(a<b && a==b) // Greater than equal to
		{
			System.out.println("True");
		}
		else
		{
			System.out.println("False");
		}
	}

}
